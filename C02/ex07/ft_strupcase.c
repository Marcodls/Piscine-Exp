#include <unistd.h>

char	*ft_strupcase(char *str)
{
	int	a;

	a = 0;
	while (str[a] != '\0')
	{
		if (str[a] > 96 && str[a] < 123)
		{
			(str[a] = str[a] - 32);
		}
		a++;
	}
	return (str);
}
